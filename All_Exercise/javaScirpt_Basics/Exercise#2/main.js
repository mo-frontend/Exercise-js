let a = 15;
let b = "15";

// Equal to
console.log(a == b); // true
// Not Equal to
console.log(a != b); // false
// Strictly Equal
console.log(a === b); // false
// Strictly Not Equal
console.log(a !== b); // true
// Greater than
console.log(a > b); // false
// less than
console.log(a < b); // false
// Greater than or Equal
console.log(a >= b); // true
// less than or Equal
console.log(a <= b); // true
