// Basic function

function add(nam1, nam2) {
  return nam1 + nam2;
}
console.log(add(3, 4)); // 7
console.log(add(6, 9)); // 15
console.log(add(9, 4)); // 13
console.log(add(2, 4)); // 6
console.log(add(310, 4)); // 314

